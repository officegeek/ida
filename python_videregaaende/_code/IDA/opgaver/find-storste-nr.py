# Finde det største nummer

# Input nummer
nr1 = int(input("Skriv det første nummer:"))
nr2 = int(input("Skriv det andet nummer:"))

# Print resultat
if nr1 > nr2:
    print(f"Det største nummer er 'nummer 1' som har værdien {nr1}")
else:
    print(f"Det største nummer er 'nummer 2' som har værdien {nr2}")